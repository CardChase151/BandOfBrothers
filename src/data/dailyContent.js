// Helper function to get today's content based on day of year
export const getTodaysContent = () => {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now - start;
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay);

  // Use modulo to cycle through content if we have less than 365 days
  const index = dayOfYear % dailyContent.length;
  return dailyContent[index];
};

// Helper function to get content for a specific day (useful for testing)
export const getContentForDay = (dayIndex) => {
  return dailyContent[dayIndex % dailyContent.length];
};
